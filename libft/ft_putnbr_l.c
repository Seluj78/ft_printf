#include "libft.h"
#include <unistd.h>

void	ft_putnbr_l(long n)
{
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_l(n / 10);
		ft_putnbr_l(n % 10);
	}
	else
		ft_putchar(n + 48);
}
